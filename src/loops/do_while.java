package loops;

public class do_while {

	public static void main(String[] args)
	{
		//I want table of 10
		//initialization-->10
		//condition--> <=100
		//updation-->i=i+10
        
		int m=10;
		do 
		{
		  System.out.println(m);
		  m=m+10;//10,20,30,40....100
		}
		while(m<=100);//20,30
		
	}

}
